<h2>1. Overview</h2>
<p>This Acceptable Use Policy ("AUP") describes what you may and may not do with the f29.us Dynamic QR
Service. It applies to all users. Violations may result in link disabling, account suspension, or
permanent termination.</p>

<h2>2. Prohibited Destinations and Content</h2>
<p>You must not use f29.us short links, QR codes, or uploaded branding assets (including QR logo images) to direct users to, or to represent, content or services that:</p>

<ul class="ul-content">
    <li><strong>Phishing</strong> — impersonate websites, services, or individuals to steal credentials or personal information</li>
    <li><strong>Malware</strong> — distribute malicious software, ransomware, spyware, or any harmful code</li>
    <li><strong>Credential theft</strong> — collect usernames, passwords, or payment information deceptively</li>
    <li><strong>Spam</strong> — support unsolicited bulk messaging or advertising campaigns</li>
    <li><strong>Deceptive redirects</strong> — mislead users about where a link leads</li>
    <li><strong>Impersonation</strong> — falsely represent an organization, person, or brand</li>
    <li><strong>Harassment</strong> — direct users to content intended to harass, threaten, or harm specific individuals</li>
    <li><strong>Illegal content</strong> — link to content that violates applicable laws, including but not limited to child sexual abuse material, content that facilitates violence, or content that infringes copyright or other intellectual property rights</li>
    <li><strong>Fraud</strong> — facilitate financial fraud, scams, or deceptive commercial practices</li>
    <li><strong>Sanctions evasion</strong> — circumvent legal sanctions or export controls</li>
    <li><strong>Infringing branding</strong> — upload logo or image assets that infringe intellectual property rights, impersonate another organization or brand, contain illegal or abusive content, or are used deceptively</li>
</ul>

<h2>3. Prohibited Platform Behavior</h2>
<p>You must not:</p>
<ul class="ul-content">
    <li>Attempt to evade domain blocklists by using redirectors, URL shorteners, or other intermediaries to reach a blocked destination</li>
    <li>Create accounts or QR codes for the purpose of circumventing a prior suspension or account action</li>
    <li>Abuse the redirect or analytics infrastructure (e.g. automated scanning to inflate analytics counts)</li>
    <li>Attempt to probe, test, or exploit the Service for security vulnerabilities without explicit written permission</li>
    <li>Interfere with other users' access to the Service</li>
</ul>

<h2>4. Enforcement</h2>
<p>f29.us may, without prior notice where urgent action is required:</p>
<ul class="ul-content">
    <li>Disable individual short links that are found to violate this AUP</li>
    <li>Add offending domains to the service-wide blocked-domain list</li>
    <li>Suspend or terminate accounts engaged in repeated or serious violations</li>
</ul>
<p>We will make reasonable efforts to notify affected account holders except where doing so would impede
an ongoing abuse response or is otherwise impractical.</p>

<h2>5. No Automated Malware Scanning</h2>
<p>f29.us does not currently perform automated scanning of destination URLs for malware or phishing.
Users are responsible for ensuring their destinations comply with this policy. We rely on user reports
and manual review to identify violations.</p>

<h2>6. Reporting Violations</h2>
<p>If you encounter a f29.us short link being used for phishing, malware, or other abuse, please
<a href="/abuse">report it</a>.</p>

<h2>7. Changes to This Policy</h2>
<p>We may update this AUP from time to time. Continued use of the Service constitutes acceptance of
the revised policy. We will update the date at the top of this page when changes are made.</p>

